public class Main {
    public static void main(String[] args)
    {
        int contador = 10;

        // operador mientras (while)
        while (contador >0)
        {
            System.out.println(contador);
            // forma larga, se usa al inicio cuando recien se aprende
            contador = contador - 1;

            // forma larga que se usa cuando ya se tiene experiencia
            // contador--;
        }
    }
}